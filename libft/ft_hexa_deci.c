#include "libft.h"

int	ft_hexa_deci(int n)
{
	int	l;

	l = 0;
	if (n == -2147483648)
	{
		l += ft_putstr("-2147483648");
		return (l);
	}
	if (n < 0)
	{
		l += ft_printchar('-');
		n = n * -1;
	}
	if (n > 9)
		l += ft_hexa_deci(n / 10);
	l += ft_printchar((n % 10) + '0');
	return (l);
}
