#include "libft.h"

int	ft_hexa_uper(unsigned int n)
{
	char	*s;
	char	l;

	l = 0;
	s = "0123456789ABCDEF";
	if (n > 15)
		l += ft_hexa_uper(n / 16);
	l += ft_printchar(s[n % 16]);
	return (l);
}
