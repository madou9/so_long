#include "libft.h"

int	ft_hexa_low(unsigned int n)
{
	char	*s;
	char	l;

	s = "0123456789abcdef";
	l = 0;
	if (n > 15)
		l += ft_hexa_low(n / 16);
	l += ft_printchar(s[n % 16]);
	return (l);
}
