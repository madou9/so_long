#include "libft.h"

int	ft_unsigned_deci(unsigned int c)
{
	int	l;

	l = 0;
	if (c > 9)
		l += ft_unsigned_deci(c / 10);
	l += ft_printchar((c % 10) + '0');
	return (l);
}
